bpPrompt = lambda bp: f'''
    以下是一份商业计划书\n
    {bp}
    --
    请试着从多个维度对其进行分析以下是一些关键的评价角度和具体内容,给出分析报告时记得加上标题：

---

### 1. **商业机会和市场分析**
   - **市场需求**：是否明确阐述了产品或服务的市场需求？有没有数据支持？
   - **市场规模**：目标市场有多大？市场存在增长潜力吗？
   - **目标受众**：是否清晰定义了目标客户群体？是否对受众有深刻的洞察？
   - **竞争分析**：是否分析了主要竞争对手及其优势、劣势？市场竞争环境是否明晰？
   - **行业趋势**：是否了解并分析行业发展的趋势和机会？

---

### 2. **商业模式**
   - **价值主张**：产品或服务是否具有独特的价值主张？如何解决客户痛点？
   - **收入来源**：收入模式是否清晰（如销售收入、订阅收入、广告收入等）？收入渠道是否多样化？
   - **成本结构**：是否清晰列出了主要成本来源？成本是否可控？
   - **盈利模型**：在现有业务模式下，是否能够实现盈利？有没有清晰的路径或逻辑？
   - **核心能力**：公司有哪些竞争优势？是否具有核心资源或独特能力？

---

### 3. **产品/服务**
   - **创新性**：产品/服务是否具备显著的创新点或差异化？
   - **技术可行性**：如果涉及技术，技术可行性是否已验证？是否存在知识产权或专利保护？
   - **用户体验**：产品或服务是否注重用户体验和反馈？
   - **发展阶段**：产品处于什么阶段（概念、原型、测试、上市）？是否有清晰的开发和推广计划？
   - **可扩展性**：产品或服务是否容易扩展到其他市场、客户或用途？

---

### 4. **团队能力**
   - **团队背景**：创始人和核心成员是否具有相关行业经验、专业能力或成功案例？
   - **执行力**：团队的执行力如何？是否能有效推动计划的落地？
   - **团队角色**：是否具备关键岗位的成员（如技术、市场、运营等）？
   - **顾问和支持**：是否有行业专家、导师或投资人支持？

---

### 5. **财务计划**
   - **财务数据**：是否清晰列出了预计收入、成本、利润等财务数据？
   - **资金使用计划**：融资需求是否合理？资金将如何分配？
   - **现金流管理**：是否提供了现金流预测？现金流能否支撑业务发展？
   - **盈利预测**：是否有现实的盈利时间表？预测是否与行业估值或增长率匹配？
   - **财务风险**：是否识别和评估了潜在的财务风险？

---

### 6. **风险与挑战**
   - **风险识别**：是否全面识别了业务可能面临的市场、技术、法规等风险？
   - **应对策略**：是否提出了有效的风险应对策略？
   - **行业壁垒**：是否面临较高的进入壁垒或退出壁垒？
   - **竞争压力**：能否应对市场竞争加剧的挑战？

---

### 7. **运营计划**
   - **推广策略**：是否有清晰的市场推广计划？营销方式和渠道是否明确？
   - **运营目标**：短期和长期的运营目标是否明确且可量化？
   - **资源配置**：资源分配是否合理（如人力、财力、设备等）？
   - **扩展策略**：未来业务扩展的策略是否清晰？是否存在多样化的可能？

---

### 8. **逻辑性与执行力**
   - **目标的逻辑性**：计划书中的业务目标、战略、方法是否有内在逻辑联系？
   - **可行性**：业务模式、实施步骤是否符合现实情况？
   - **时间表**：是否有清晰的时间规划，列明关键的里程碑？
   - **实施路径**：是否有明确的实施路径或步骤？

---

### 9. **投资吸引力**
   - **预期回报**：对投资人来说，回报是否具有吸引力？
   - **退出机制**：是否明确列出投资人的退出策略（如并购、上市或股权回购）？
   - **估值合理性**：公司估值是否符合行业标准？
   - **增长潜力**：未来业务的成长性和可持续性如何？
'''

